export function t(
	language: UiLanguage,
	key: string,
	params?: TranslationParams,
): string {
	const value = TRANSLATIONS[language][key] ?? TRANSLATIONS.en[key] ?? key;
	return typeof value === "function" ? value(params ?? {}) : value;
}

export function formatSyncSummary(counters: SyncCounters, language: UiLanguage): string {
	return [
		t(language, "countersUploaded", { count: counters.uploaded }),
		t(language, "countersDownloaded", { count: counters.downloaded }),
		t(language, "countersDeletedLocal", { count: counters.deletedLocal }),
		t(language, "countersDeletedRemote", { count: counters.deletedRemote }),
		t(language, "countersConflicts", { count: counters.conflicts }),
		t(language, "countersUnchanged", { count: counters.unchanged }),
	].join(", ");
}
